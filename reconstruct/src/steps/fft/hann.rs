//! Hann window generation

use std::f32::consts::PI;
use std::iter::FusedIterator;

/// An iterator that produces a Hann window
///
/// The window will be zero at its beginning, one in the middle, and approximately zero
/// at its end.
pub struct HannWindow {
    /// Index of the next value to return
    next: usize,
    /// Total number of values to return
    length: usize,
}

impl HannWindow {
    /// Creates a Hann window of the specified length
    pub fn new(length: usize) -> Self {
        HannWindow { next: 0, length }
    }
}

impl Iterator for HannWindow {
    type Item = f32;

    fn next(&mut self) -> Option<Self::Item> {
        if self.next == self.length {
            None
        } else {
            let next = self.next as f32;
            let length = self.length as f32;
            let value = 0.5 * (1.0 - f32::cos(PI * 2.0 * next / length));
            self.next += 1;
            Some(value)
        }
    }

    fn size_hint(&self) -> (usize, Option<usize>) {
        let remaining = self.length - self.next;
        (remaining, Some(remaining))
    }
}

impl FusedIterator for HannWindow {}

impl ExactSizeIterator for HannWindow {}

